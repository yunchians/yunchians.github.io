// QRCODE網址連結
function openQRcodeURL() {
    const qrcodeURL = 'https://zalo.me/0563031570'; // QRCODE的網址設定
    if (qrcodeURL == '') {
        return;
    } else {
        window.open(qrcodeURL);
    }
}

