// QRCODE網址連結
function openQRcodeURL() {
    const qrcodeURL = 'https://lin.ee/D6Iuj0R'; // QRCODE的網址設定
    if (qrcodeURL == '') {
        return;
    } else {
        window.open(qrcodeURL);
    }
}

